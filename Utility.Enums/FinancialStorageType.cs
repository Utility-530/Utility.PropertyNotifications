
namespace Utility.Enums
{
    public enum FinancialStorageType
    {
        BankAccount,        // Stored in a traditional bank account
        BrokerageAccount,   // Held in a brokerage or trading account
        DigitalWallet,      // Stored in a digital wallet (e.g., PayPal, Venmo, crypto wallets)
        SafeDepositBox,     // Physically stored in a secure bank vault
        CustodialAccount,   // Held by a custodian on behalf of the owner
        RetirementAccount,  // Stored in retirement plans (e.g., IRA, 401(k))
        CryptoExchange,     // Held on a cryptocurrency exchange
        PhysicalVault,      // Stored in a private or institutional vault
        OffshoreAccount,    // Held in a foreign financial institution
        InsurancePolicy     // Embedded within an insurance product (e.g., cash value life insurance)
    }

}
